                <div class="px-lg-5">
                    <p>Desde la Asamblea destacan que, además de no contar con licencia social, Portezuelo es una obra
                        ilegal. Los datos hídricos utilizados para diseñar el proyecto son de hace más de 40 años, es
                        decir, con un caudal distinto al actual e ignorando los efectos del cambio climático; tampoco se
                        hizo un estudio de impacto ambiental de toda la cuenca (que nace en Mendoza y Neuquén y llega
                        hasta el partido de Villarino, en el sur bonaerense) y por si fuera poco está incumpliendo leyes
                        nacionales y el derecho a consulta previa a los pueblos originarios, entre otras
                        irregularidades.</p>
