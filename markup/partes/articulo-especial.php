                            Colorado. Investigando nos dimos cuenta que la principal función de Portezuelo no es generar
                            energía para los hogares de Mendoza, sino que su desarrollo está atado a los pozos de
                            fracking y
                            a la minería de potasio que requieren de mucha agua”</span>, afirma Miguel Di Ferdinando,
